/**
 * nvm的介绍和使用：
 * 1. nvm全称【node version manager】，顾名思义它是用来管理node版本的工具，方便切换不同版本的Node.js
 * 
 * 2. nvm的使用非常简单，跟npm的使用方法类似，都是通过命令行窗口来使用
 * 
 * 3. 首先先下载nvm，下载地址【https://github.com/coreybutler/nvm-windows/releases】
 * 4. 选择【nvm-setup.exe】下载即可
 * 
 * 5. 常用命令：
 *    nvm list available         -->    显示所有可以下载的Node.js版本
 *    nvm list                   -->    显示已安装的版本
 *    nvm install 18.12.1        -->    安装18.12.1版本的Node.js
 *    nvm install latest         -->    安装最新版的Node.js
 *    nvm uninstall 18.12.1      -->    删除某个版本的Node.js
 *    nvm use 18.12.1            -->    切换18.12.1的Node.js
 * 
 */